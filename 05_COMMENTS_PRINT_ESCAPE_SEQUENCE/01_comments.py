# comments are used to explain the code and are not executed
# there are two types of comments in Python:
# 1. Single-line comments start with a hash symbol (#)
# 2. Multi-line comments can be created using triple quotes (''' or """)

# This is a single-line comment
print("Hello, World!")  # This is an inline comment after a statement

'''This is a multi-line comment
It can span multiple lines'''
print("This is a multi-line comment example")  # This will print a message