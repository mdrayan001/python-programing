# escape sequences are used to insert special characters in strings

# common escape sequences:

# \n - New line
print("Hello,\nWorld!")  # This will print "Hello," on one line and "World!" on the next line

# \t - Tab
print("Hello,\tWorld!")  # This will print "Hello," followed by a tab space and then "World!"

# \\ - Backslash
print("This is a backslash: \\")  # This will print "This is a backslash: \"

# \' - Single quote
print('This is a single quote: \'')  # This will print "This is a single quote: '"

# \" - Double quote
print("This is a double quote: \"")  # This will print "This is a double quote: "

# \r - Carriage return
print("Hello,\rWorld!")  # This will overwrite "Hello," with "World!" on the same line
