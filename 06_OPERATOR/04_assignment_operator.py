# Assignment operators in Python

# = , +=, -=, *=, /=, //=, %=, **=

a = 10
b = 5

a += b  # a = a + b
print(a)  # Output: 15
a -= b  # a = a - b
print(a)  # Output: 10
a *= b  # a = a * b
print(a)  # Output: 50
a /= b  # a = a / b
print(a)  # Output: 10.0
a //= b  # a = a // b
print(a)  # Output: 2.0
a %= b  # a = a % b
print(a)  # Output: 0.0
a **= b  # a = a ** b
print(a)  # Output: 0.0 (since 0 raised to any power is 0)
