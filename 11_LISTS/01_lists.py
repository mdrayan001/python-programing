# List are ordered, mutable, and allow duplicate elements.
# Lists are defined using square brackets [].

# Example of a list
marks = [90, 85, 78, 92, 88]
mixed = [1, "Hello", 3.14, True]

print(marks)
# Accessing elements in a list
print(marks[0])  # First element
print(marks[1:3])  # Slicing from index 1 to 2

print(mixed)
# Accessing elements in a mixed list
print(mixed[2])  # Accessing the float element
print(mixed[0:3]) # Slicing the first three elements