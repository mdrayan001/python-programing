# conditional operators

a = 10
b = 20

print(a == b)  # Equal to
print(a != b)  # Not equal to, print false
print(a > b)   # Greater than, print false
print(a < b)   # Less than, print true
print(a >= b)  # Greater than or equal to, print false
print(a <= b)  # Less than or equal to, print true
