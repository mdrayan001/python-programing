a = 34    # here a is an integer
b = "34"  # here b is a string

print(a)
print(type(a))

print(b)
print(type(b))

# Converting string to integer
c = int(b)
print(c)
print(type(c))

# Converting integer to string
d = str(a)
print(d)
print(type(d))

# Converting float to integer
e = 3.14
f = int(e)
print(f)
print(type(f))
